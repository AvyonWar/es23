public class ArithmeticOperators {


        /**
         * somma
         */
        public int sum(int a, int b) {
            int risultato = a + b;
            return risultato;
        }

        /**
         * sottrazione
         */
        public int sub(int a, int b){
            int risultato = a - b;
            return risultato;
        }

        /**
         * moltiplicazione
         */
        public int mult(int a, int b){
            int risultato = a * b;
            return risultato;
        }

        /**
         * divisione
         */
        public int div(int a, int b){
            int risultato = a / b;
            return risultato;
        }
    }

